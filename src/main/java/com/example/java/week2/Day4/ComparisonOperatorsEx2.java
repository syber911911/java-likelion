package com.example.java.week2.Day4;

public class ComparisonOperatorsEx2 {
    public static void main(String[] args) {
        int iVal1 = 10;
        int iVal2 = 20;
        System.out.printf("iVal1 == iVal2 ? %b\n", iVal1 == iVal2);
        System.out.printf("iVal1 != iVal2 ? %b\n", iVal1 != iVal2);
        System.out.printf("iVal1 < iVal2 ? %b\n", iVal1 < iVal2);
        System.out.printf("iVal1 > iVal2 ? %b\n", iVal1 > iVal2);
        System.out.printf("iVal1 <= iVal2 ? %b\n", iVal1 <= iVal2);
        System.out.printf("iVal1 >= iVal2 ? %b\n", iVal1 >= iVal2);
    }
}
