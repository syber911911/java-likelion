package com.example.java.week2.Day1;

public class DivideIntoTwo {
    public static void main(String[] args) {
        float fOne = 1; // 1.0f
        float fTow = 2;
        System.out.printf("%f\n", fOne + fOne / fTow);

        int iOne = 1;
        int iTwo = 2;
        System.out.println(iOne + iOne / iTwo);

        System.out.println(9 / 2);
        System.out.println(9 / 2.0);
    }
}
